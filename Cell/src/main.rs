//`Cell<T>` 提供内部可变性，但与 `RefCell<T>` 不同，它通过**值的移动和拷贝**来实现，而非借用。
// 他的trait 约束是必须实现Copy

// 在 Rust 中，`Cell` 和 `RefCell` 都属于**内部可变性（Internal Mutability）**模式。它们允许你在拥有不可变引用（`&T`）的情况下修改内部数据。
//
// 这听起来违反了 Rust 的核心借用规则，但它们通过不同的机制确保了安全性。
//
// ---
//
// ### 1. 核心区别对照表
//
// | 特性 | `Cell<T>` | `RefCell<T>` |
// 特性,              Cell<T>,                                RefCell<T>
// 操作方式,          值复制。通过 get/set 移动或复制值。       引用借用。通过 borrow/borrow_mut 获取指针。
// 检查时机,          编译时。无额外运行时开销。                行时。通过引用计数检查借用规则。
// 性能,              极高。几乎等同于直接修改值。              略低。有记录借用状态的运行时开销。
// 适用类型,         "实现了 Copy 的简单类型（如 i32, bool）。 复杂类型（如 Vec, String 或自定义结构体）。"
// 报错方式           不会报错（因为没有借用检查）。              运行时若违反借用规则会 Panic。

// ---
//
// ### 2. `Cell<T>`：简单的“替换”逻辑
//
// `Cell` 并不给你内部数据的引用，它只是让你把值“拿出来”或“放进去”。因为它不产生引用，所以不存在“多个可变引用”的问题。
//
//
//
// ```rust

// ```
// * **适用场景**：小型的 `Copy` 类型，或者你不需要获取内部数据引用，只需要替换值的情况。
//
// ---
//
// ### 3. `RefCell<T>`：运行时的“借用检查器”
//
// `RefCell` 允许你获取内部数据的引用（`&T` 或 `&mut T`）。它把 Rust 编译器的借用检查规则从**编译阶段推迟到了运行阶段**。
//
//
//
// ```rust
// use std::cell::RefCell;
//
// let r = RefCell::new(vec![1, 2, 3]);
//
// 获取临时可变引用
// r.borrow_mut().push(4);
//
// 注意：如果你在已经有一个 borrow_mut 的情况下再次调用 borrow_mut，
// 程序会在运行时 Panic，而不是编译失败。
// let m1 = r.borrow_mut();
// let m2 = r.borrow_mut(); // 这行会导致运行时程序崩溃！
// ```
// * **适用场景**：当你确信逻辑上不会同时存在多个可变引用，但编译器由于太“死板”无法证明这一点时（常见于 Web 框架的上下文对象或 UI 组件库）。
//
// ---
//
// ### 4. 总结与选择建议
//
// * 如果你处理的是 `i32`、`f64`、`bool` 等简单类型，**首选 `Cell`**，因为它既安全又快。
// * 如果你处理的是没有实现 `Copy` 的复杂对象（如 `Struct` 或 `Vec`），且需要获取它们的引用，**必须用 `RefCell`**。
// * **注意**：这两者都是**非线程安全**的（没有实现 `Sync`）。如果你需要在多线程环境下实现类似功能，请回到我们之前讨论的 **`Mutex<T>`** 或 **`RwLock<T>`**。
use std::cell::Cell;
use std::cell::RefCell;
fn main() {
    println!("Hello, world!");

    let c = Cell::new(5);
    let shared_ref = &c;

    shared_ref.set(10); // 即使引用是不可变的，依然可以修改内部值
    println!("Value: {}", c.get()); // 输出 10



    let r = RefCell::new(vec![1, 2, 3]);

    // 获取临时可变引用,这是借用
    r.borrow_mut().push(4);

    // 注意：如果你在已经有一个 borrow_mut 的情况下再次调用 borrow_mut，
    // 程序会在运行时 Panic，而不是编译失败。
    let m1 = r.borrow_mut();
    // let m2 = r.borrow_mut(); // 这行会导致运行时程序崩溃！
}
